#include "libft.h"

int	ft_ispath(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (0);
	if (str[0] == '/')
		return (1);
	while (str[i] == '.')
		i++;
	if (str[i] == '/')
		return (1);
	return (0);
}
